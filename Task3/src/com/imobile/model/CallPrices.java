package com.imobile.model;

public class CallPrices {
    private double withinNetwork;
    private double otherNetwork;
    private double landlines;
    
    public double getWithinNetwork() {
        return withinNetwork;
    }
    public void setWithinNetwork(double withinNetwork) {
        this.withinNetwork = withinNetwork;
    }
    public double getOtherNetwork() {
        return otherNetwork;
    }
    public void setOtherNetwork(double otherNetwork) {
        this.otherNetwork = otherNetwork;
    }
    public double getLandlines() {
        return landlines;
    }
    public void setLandlines(double landlines) {
        this.landlines = landlines;
    }
    
}
